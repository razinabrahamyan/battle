<script>
    $(document).ready(function () {
        $('#register_password').on('keyup', function () {
            let pass = $(this).val();
            let strength = 1;
            let arr = [/.{8,}/, /[a-z]+/, /[0-9]+/, /[A-Z]+/];
            let class_strength = $('.password_strength');
            jQuery.map(arr, function(regexp) {
                if(pass.match(regexp))
                {
                    strength++;
                    $('#password_strength_val').val(strength);
                    if(strength < 4) {
                        class_strength.html('weak');
                        class_strength.removeClass('text-yellow');
                        class_strength.removeClass('text-success');
                        class_strength.addClass('text-danger');
                    } else if(strength === 4) {
                        $('.password_strength').html('average');
                        class_strength.removeClass('text-danger');
                        class_strength.removeClass('text-success');
                        class_strength.addClass('text-yellow')
                    } else  {
                        class_strength.html('strong');
                        class_strength.removeClass('text-danger');
                        class_strength.removeClass('text-yellow');
                        class_strength.addClass('text-success')
                    }
                }

            });
            console.log(strength, 'strength')
        });
    });

</script>
